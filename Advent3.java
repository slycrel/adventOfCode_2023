	final static String symbols = "*/=+@$%-&#";     // I cheated -- scraped out the other text in bbedit until I found all the symbols.  Could have been done programmatically if I needed to.
	final static char ignore = '.';

	record FoundNumber(int offset, String number)  { int getNum() { return Integer.parseInt(number); } }

	record Gear(FoundNumber one, FoundNumber two) { }

	final static ArrayList<Gear> gears = new ArrayList<>();

	public static void main(String[] args)
	{
		// plan:
		// on the string, scan for line length, remember line offset
		// scan thru each character.  When a symbol is hit,
		//     check the immediate before and after characters and
		//     also the 3 on the prior line and next line (curr offset -1 and +1 and also -1 line, +1 line the same way including the current position)
		//         check for beginning and ending of lines, don't check before or after the line!
		//     when numbers are found, extract the found number and total it
		//         if the number extends into multiple positions, don't count it more than once!
		// sum up the numbers that are found

		var input = extraLine + actualInput + extraLine;
//		var input = testInput;

		var sumNumbers = new HashSet<FoundNumber>();
		int lineLength = -1;
		int line = 0;
		var buffer = input.toCharArray();

		for (int x = 0; x < buffer.length; x++)
		{
			// skip periods
			if (ignore == buffer[x] || Character.isDigit(buffer[x]))
				continue;

			// skip line endings, set line length ont he first found
			if (buffer[x] == '\n') {
				if (lineLength == -1)
					lineLength = x + 1;
				System.out.print("line " + line + " ends, actual line: " + new String(buffer, lineLength * line, lineLength));
				line++;
				continue;
			}

			if (symbols.contains(String.valueOf(buffer[x])))
			{
				// check for collisions!  Based on eyeballing the input, no numbers double-collide
				ArrayList<FoundNumber> nums = findAssociatedCollisionNumbers(x, buffer, lineLength);
				sumNumbers.addAll(nums);
			}
		}

		// sum the collision numbers
		int sum = 0;
		for (var foundNum : sumNumbers)
		{
//			System.out.println("offset " + foundNum.offset() + " num: " + foundNum.number());
			sum += foundNum.getNum();
		}
		System.out.println("sum total: " + sum);

		// part 2
		// gear ratio is found by multiplying the 2 part #s together
		int gearRatioSum = 0;
		for (var gear : gears)
		{
			gearRatioSum += gear.one().getNum() * gear.two().getNum();
		}
		System.out.println("gear ratio sum total: " + gearRatioSum);
	}

	private static ArrayList<FoundNumber> findAssociatedCollisionNumbers(int offset, char[] buffer, int lineLength)
	{
		var retVal = new ArrayList<FoundNumber>();

		// current line
		var priorChar = buffer[offset - 1];
		var nextChar = buffer[offset + 1];

		// part 1
		if (Character.isDigit(priorChar))
			retVal.add(parseFoundDigit(offset - 1, buffer));
		if (Character.isDigit(nextChar))
			retVal.add(parseFoundDigit(offset + 1, buffer));

		// part 2
		// line above
		associatedCollisions(offset, buffer, lineLength, retVal, true);

		// line below
		associatedCollisions(offset, buffer, lineLength, retVal, false);

		// part 2!
		if (buffer[offset] == '*')
		{
			if (retVal.toArray().length == 2)
				gears.add(new Gear(retVal.get(0), retVal.get(1)));
		}

		return retVal;
	}

	private static void associatedCollisions(int offset, char[] buffer, int lineLength, ArrayList<FoundNumber> retVal, boolean onLineAbove)
	{
		if (onLineAbove)
			lineLength *= -1;   // subtract lineLength for above, add lineLength for below

		var diagLeftAbove = buffer[offset - 1 + lineLength];
		var above = buffer[offset + lineLength];
		var diagRightAbove = buffer[offset + 1 + lineLength];

		FoundNumber diagLeftNum = null;
		FoundNumber middleNum = null;
		FoundNumber diagRightNum = null;
		if (Character.isDigit(diagLeftAbove))
			diagLeftNum = parseFoundDigit(offset - 1 + lineLength, buffer);
		if (Character.isDigit(above))
			middleNum = parseFoundDigit(offset + lineLength, buffer);
		if (Character.isDigit(diagRightAbove))
			diagRightNum = parseFoundDigit(offset + 1 + lineLength, buffer);

		// left and right found
		if (diagLeftNum != null && diagRightNum != null)
		{
			if (middleNum != null)       // all 3 are digits, same number!
			{
				retVal.add(middleNum);
			}
			// diff numbers, so separated already, above can't be a #
			else if (diagRightNum.getNum() != diagLeftNum.getNum())
			{
				retVal.add(diagLeftNum);
				retVal.add(diagRightNum);
			}
			else    // they are the same, add one
			{
				retVal.add(diagLeftNum);
			}
		}
		else if (middleNum != null)
		{
			// they have to be the same # as either the left or right diag numbers
			// we have already determined this case both diags are not digits, so we always
			// are either a single digit above or touching one or the other diag -- same add for all cases
			retVal.add(middleNum);
		}
		else if (diagLeftNum != null)
		{
			retVal.add(diagLeftNum);
		}
		else if (diagRightNum != null)
		{
			retVal.add(diagRightNum);
		}
	}

	private static FoundNumber parseFoundDigit(int offset, char[] buffer)
	{
		var begin = offset;
		var end = offset + 1;

		while (Character.isDigit(buffer[begin - 1]))
			begin--;

		while (Character.isDigit(buffer[end]))
			end++;

		return new FoundNumber(begin, new String(buffer, begin, end - begin));
	}

